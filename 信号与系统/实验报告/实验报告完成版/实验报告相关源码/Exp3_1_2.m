t=-1:0.01:1;%%-1开始1终止步长0.01
p=0.01;%%初始化步长
k1=0:p:4;%%0开始4终止步长p
f1=2*ones(1,length(k1));%%生成矩阵
k2=0:p:2;%%0开始2终止步长p
f2=ones(1,length(k2));
[f,k]=sconv(f1,f2,k1,k2,p);

function [f,k]=sconv(f1,f2,k1,k2,p)
f=conv(f1,f2);%%计算需要序列f1与f2的卷积和f
f=f*p;%%将f进行采样
k0=k1(1)+k2(1);%%计算序列f非零样值的起点位置
k3=length(f1)+length(f2)-2;%%计算卷积和f的非零样值的宽度
k=k0:p:k3*p;%%计算卷积和f的非零样值的时间向量
subplot(2,2,1)%%绘制图案
plot(k1,f1)%%绘制离散数据
title('f1(t)')%%设置标题
xlabel('t')%设置自变量
ylabel('f1(t)')%%设置因变量
subplot(2,2,2)%%绘制图案
plot(k2,f2);%%绘制离散数据
title('f2(t)')%%设置标题
xlabel('t')%设置自变量
ylabel('f2(t)')%%设置因变量
subplot(2,2,3)%%绘制图案
plot(k,f)%%绘制离散数据
title('f(t)=f1(t)*f2(t)')%%设置标题
xlabel('t')%设置自变量
ylabel('f(t)')%%设置因变量
end